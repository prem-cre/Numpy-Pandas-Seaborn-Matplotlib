import  numpy as np

# ZERO
zero = np.zeros(4)
zero1 = np.zeros((3,4))
print(zero)
print(zero1)

# ONES
ones = np.ones(4)
print(ones)

# EMPTY
EMPTY = np.empty(4)
print(EMPTY)                    # prints before runned code

# RANGE
range = np.arange(4)
print(range)

# DIAGONAL
DIA = np.eye(3)
print(DIA)

# LINSPACE
LINSPACE = np.linspace(0,20,num = 5)
print(LINSPACE)
